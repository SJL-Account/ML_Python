def createC1(dataSet):
    '''

    :param D:数据库记录
    :return: k=1的候选集
    '''

    C1=[]

    for transition in dataSet:
        for item in transition:
            if not [item] in C1:
                C1.append([item])
    C1.sort()

    return tuple(C1)#map(frozenset,C1)

def scanD(D ,Ck ,min_support):
    '''

    :param D: set类型 数据库记录
    :param Ck: frozenset类型 k候选集
    :param min_support:  最小支持度
    :return: Ck+1 ,支持度信息
    '''
    ssCnt={}
    reList=[]
    supportData={}
    # 算法步骤
    # 1.检测时候选集Ck中每一条是否

    # 对于数据库中的每一条记录
    for tid in D:
        # 对于每个候选集
        for can in Ck:
            # 如果候选集是这条记录的子集
            if set(can).issubset(set(tid)):
                # 如果没在ssCnt中
                if tuple(can) not in ssCnt:
                    ssCnt[tuple(can)]=1
                else:
                    ssCnt[tuple(can)]+=1

    numsCount = float(len(D))

    # 2.判断是否满足最小支持度
    for key in ssCnt:
        support=ssCnt[key]/numsCount
        if support>=min_support:
            reList.append(key)
        supportData[key]=support
    return reList,supportData


def aprioriGen(Lk,k):
    '''
    由Lk产生Ck+1
    :param Lk:k>1的频繁项集
    :param k:k+1 希望生成的集合的k值
    :return: Ck+1候选集 
    '''

    Cklen=len(Lk)
    reList=[]
    for i in range(Cklen):
        for j in range(i+1,Cklen):
            #提取出除最后一项的所有项
            C1=list(Lk[i])[:k-2]
            C2=list(Lk[j])[:k-2]
            #排序 是为了标准序列
            C1.sort()
            C2.sort()
            if C1==C2:
                reList.append(tuple(set(Lk[i])|set(Lk[j])))

    return reList


def Apiroir(dataSet,min_support):

    '''
    
    :param dataSet:数据记录
    :param min_support:最小支持度
    :return:所有的频繁项集、所有项的支持度记录
    '''

    D = tuple(dataSet)#map(set, dataSet)

    #C1开始扫描
    C1=createC1(dataSet)

    #获得L1以及原始的支持度信息

    L1,supportData=scanD(D,C1,min_support)

    k=2
    
    #初始化将L1装进去 L=[[1项集],[2项集],,,[k项集]]
    L=[L1]
   
    #当集合中的最后一项不为空时(能产生k项集)
    while len(L[k-2])>0:
	#Lk->Ck+1
        Ck=aprioriGen(L[k-2],k)
	#Ck+1->Lk+1
        Lk,supportDatak=scanD(D, Ck, min_support)
	#更新支持度
        supportData.update(supportDatak)
	
        L.append(Lk)

        k+=1

    return  L,supportData

def CalcConf(freSet,H,supportData,brl,min_conf):

    '''
    根据频繁项集、支持度、可信度来过滤数据、生成规则

    :param freSet: 单个频繁项集合
    :param H :项集左部
    :param supportData: 所有项的支持度
    :param brl:大规则
    :param min_conf: 可信度

    :return:过滤后的H
    '''

    rulerH=[]
    #项集左部 H
    #项集右部 freSet-H
    for h in H:
        conf=supportData[freSet]/supportData[tuple(set(freSet)-set(h))]
        if conf>min_conf:
            print  (str(tuple(set(freSet)-set(h)))+'--->'+str(h) +'Conf:'+str(conf))
            brl.append((tuple(set(freSet)-set(h)),h,conf))
            rulerH.append(h)
    return  rulerH

def rulersfromConq(freSet, H,supportData,brl,min_conf):
    '''
    从最初的项集生成更多的关联规则
    :return:
    '''

    m=len(H[0])

    if len(freSet)>m+1: # 如果如何将m个元素从freSet中切出去
        # 从m个元素产生m+1个元素
        Hmp1=aprioriGen(H,m+1)
        # 将m+1个元素丢进 计算可信度中进行交叉生成规则
        Hmp1= CalcConf(freSet,Hmp1,supportData,brl,min_conf)
        # 如果仍有有可以生成的规则了
        if len(Hmp1)>1:
            # 递归加载
            rulersfromConq(freSet, Hmp1,supportData, brl, min_conf)



def generateRules(freSets,supportData,min_conf):
    '''
    根据频繁项集、可信度、以及支持度来生成 关联规则
    :param freSets: 频繁项集合
    :param supportData: 所有项的支持度
    :param min_conf: 可信度
    :return:
    '''

    m=len(freSets)

    #我们把最后要产生的可用的规则构成一个set的list集合
    bigRulesList=[]

    for i in range(1,m): # 对于每k项集合
        for freSet in freSets[i]: # 对于k项集合中的每一个元素

            H1=[tuple([item]) for item in freSet] # 原子化
	    
	    #大于1（3项集以上）要先产生组合，在计算可信度
            if i>1:
                rulersfromConq(freSet,H1,supportData,bigRulesList,min_conf)
            #小于1（2项集）可以直接计算
            else:
                CalcConf(freSet,H1,supportData,bigRulesList,min_conf)



